function setLang(lang) {
  const t = T[lang]; if (!t) return;
  document.documentElement.lang = lang;
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const v = t[el.dataset.i18n]; if (v !== undefined) el.textContent = v;
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const v = t[el.dataset.i18nHtml]; if (v !== undefined) el.innerHTML = v;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const v = t[el.dataset.i18nPlaceholder]; if (v !== undefined) el.placeholder = v;
  });
  const btn = document.getElementById('langToggle');
  if (btn) btn.textContent = lang === 'de' ? 'EN' : 'DE';
  localStorage.setItem('lang', lang);
}

function toggleLang() {
  setLang((localStorage.getItem('lang') || 'de') === 'de' ? 'en' : 'de');
}

document.addEventListener('DOMContentLoaded', () => {
  setLang(localStorage.getItem('lang') || 'de');
});
